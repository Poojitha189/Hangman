# The parameters we require to execute the game:
def main():
    global count
    global display
    global word
    global already_guessed
    global length
    global play_game
    global blank
    words_to_guess = ["nature","mountain","image","music","dog","water","banana","mango","carrot","orange","plants"]
    word = random.choice(words_to_guess)
    length = len(word)
    count = 0
    display = '_' * length
    already_guessed = []
    play_game = ""
    blank='_ '*length
    print("This is hangman word:"+blank)

# A loop to re-execute the game when the first round ends:

def play_loop():
    global play_game
    play_game = input("Do You want to play again? y = yes, n = no:")
    if play_game == "y":
        main()
        hangman()
    elif play_game == "n":
        exit()
        
# Initializing all the conditions required for the game:
